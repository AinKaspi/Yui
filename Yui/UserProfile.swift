import Foundation

// MARK: - Модель UserProfile
// Эта структура хранит данные пользователя: имя, аватар, уровень и достижения.
// Мы используем struct, потому что это простой тип данных, который не требует сложной логики.
struct UserProfile {
    // Имя пользователя (например, "Alex").
    let name: String
    
    // Название изображения для аватара (например, "person.circle").
    // Пока используем системные иконки (SF Symbols), позже можно заменить на кастомные изображения.
    let avatarImageName: String
    
    // Уровень пользователя (например, 5).
    let level: Int
    
    // Список достижений (например, ["Новичок", "10 приседаний"]).
    let achievements: [String]
}

// MARK: - Тестовые данные
// Расширение для UserProfile, чтобы создать тестовый профиль.
// Это позволяет нам сразу протестировать UI, не подключая реальные данные.
extension UserProfile {
    // Статическое свойство testProfile создаёт тестовый профиль.
    static let testProfile = UserProfile(
        name: "Ain", // Имя пользователя
        avatarImageName: "person.circle", // Системная иконка для аватара
        level: 7, // Уровень
        achievements: ["Новичок", "10 приседаний", "Утренний бегун"] // Список достижений
    )
}
